func titleToNumber(columnTitle string) int {

	res, val := 0, 0

	for _, v := range columnTitle {
		val = int(v - 'A' + 1)
		res = res*26 + val
	}
	return res
}
func backspaceCompare(s string, t string) bool {
	sAfter := ""
	tAfter := ""

	for _, b := range s {
		if string(b) == "#" {
			sAfter = sAfter[:len(sAfter)-1]
		} else {
			sAfter += string(b)
		}

	}

	for _, b := range t {
		if string(b) == "#" {
			tAfter = tAfter[:len(tAfter)-1]
		} else {
			tAfter += string(b)
		}

	}
	if sAfter == tAfter {
		return true
	}
	return false
}
